    private void listarNotas() {
        try {
            DefaultTableModel dtmMarca = (DefaultTableModel) jTableAddNotas.getModel();
            Conexao bd = new Conexao();
            dtmMarca.setRowCount(0);

            bd.conectarBanco();

            String sql = "select * from `nota fiscal` inner join fornecedor on idfornecedor = fornecedor_idfornecedor";
           
            if (!this.fData.equals("")) {
                sql += " where DatadeEmissao >= '"+ this.fData +"'";
            }
            
            bd.fazerConsulta(sql);

            while (bd.resultado.next()) {
                String campos[] = {bd.resultado.getString("fornecedor.nome") + "", bd.resultado.getString("cnpj"), bd.resultado.getString("idfornecedor"),
                    bd.resultado.getString("NumeroDaNota"), bd.resultado.getString("NumeroDeSerie"), bd.resultado.getString("valornota"),
                    bd.resultado.getString("DatadeEmissao")};
                dtmMarca.addRow(campos);
            }
            bd.desconectarBanco();
        } catch (Exception exc) {
            System.out.println(exc.getMessage());
        }
    }

