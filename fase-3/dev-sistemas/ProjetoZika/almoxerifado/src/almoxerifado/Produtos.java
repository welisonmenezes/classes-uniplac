    private void listarProdutos() {
        try {
            DefaultTableModel dtmMarca = (DefaultTableModel) jTableprodutos.getModel();
            Conexao bd = new Conexao();
            dtmMarca.setRowCount(0);

            bd.conectarBanco();

            String sql = "select * from produto where Status = 1";
            
            if (!this.fUnidades.equals("")) {
                sql += " and unidade = '"+ this.fUnidades +"'";
            }
            
            bd.fazerConsulta(sql);

            while (bd.resultado.next()) {
                String campos[] = {bd.resultado.getInt("idproduto") + "", bd.resultado.getString("Nome"), bd.resultado.getString("unidade"), bd.resultado.getString("Status")};
                dtmMarca.addRow(campos);
            }
            bd.desconectarBanco();
        } catch (Exception exc) {
            System.out.println(exc.getMessage());
        }
    }

