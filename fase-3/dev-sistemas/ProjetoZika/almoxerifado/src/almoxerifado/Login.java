    private void jButtonEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEntrarActionPerformed
        // TODO add your handling code here:
//        setVisible(false);
//        JFrame menu = new Menu();
//        menu.setVisible(true);
        String login = txtLogin.getText();
        String senha = new String (txtSenha.getPassword());
        
       UsuarioLogado.setId(0);
        
        try{
            Conexao bd = new Conexao();
            bd.conectarBanco();
            
            String sql = "select * from  usuario where nome = '"+login+"' and senha = '"+senha+"'";
        bd.fazerConsulta(sql);

        //adiciona as colunas
         
        while (bd.resultado.next()) {
            //pega os valores do bd para popular tabela
            UsuarioLogado.setId(bd.resultado.getInt("idusuario"));
            UsuarioLogado.setPermissao(bd.resultado.getString("Permissao"));
        }
    
        
        }
         catch (Exception ex) {
            ex.printStackTrace();
        }
        if( UsuarioLogado.getId() > 0 ){
            JOptionPane.showMessageDialog(null, "bem vindo");
             setVisible(false);
             MenuPrincipal menuPrincipal = new MenuPrincipal();
             menuPrincipal.setVisible(true);
             
             menuPrincipal.jMenuRelatorio.setVisible(false);
             if (UsuarioLogado.getPermissao().equals("Usuario")) {
                 menuPrincipal.jMenuFornecedor.setVisible(false);
                 menuPrincipal.jMenuNotas.setVisible(false);
                 menuPrincipal.jMenuProdutos.setVisible(false);
                 menuPrincipal.jMenuUsuario.setVisible(false);
                 menuPrincipal.jMenuItemPedido.setVisible(false);
             } else if (UsuarioLogado.getPermissao().equals("Almoxarife")) {
                 menuPrincipal.jMenuRelatorio.setVisible(false);
             }
        
             
        }else{
            JOptionPane.showMessageDialog(null, "Acesso negado");
            
            
        }
         LogManager.getLogger().error("usuario logado com sucesso!");
            
    }//GEN-LAST:event_jButtonEntrarActionPerformed

