    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        // TODO add your handling code here:
        String login = jTextField1.getText();
        String senha = new String(jPasswordField1.getPassword());
        try {
            Conexao bd = new Conexao();
            bd.conectarBanco();
            String sql = "select * from pedidos left join usuario on usuario.idusuario = pedidos.usuario where pedidos.idpedidos =" + this.idpedido;
            System.out.println(sql);
            bd.fazerConsulta(sql);

            if (bd.resultado.next()) {
                this.loginSolicitante = bd.resultado.getString("nome");
                this.senhaSolicitante = bd.resultado.getString("Senha");
            }
            bd.desconectarBanco();

            if (login.equals(this.loginSolicitante) && senha.equals(this.senhaSolicitante)) {
                aprovarPedido();
                JOptionPane.showMessageDialog(null, "Pedido entregue com sucesso.");
            } else {
                JOptionPane.showMessageDialog(null, "Solicitante n√£o correspodente.");
            }

        } catch (Exception exc) {
            System.out.println(exc.getMessage());
        }
    }//GEN-LAST:event_btnRetirarActionPerformed

